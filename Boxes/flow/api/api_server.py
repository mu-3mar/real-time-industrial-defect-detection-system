def _production_line_from_camera_source(camera_source: Union[str, int]) -> str:
    """
    Derive a stable production_line_id from camera_source when the client does not send one.

    Rules:
    - Numeric source 0 -> line-1, 1 -> line-2, etc.
    - /dev/videoN -> line-(N+1)
    - Other sources -> line-<normalized_source> (fallback to short hash if needed)
    """
    if isinstance(camera_source, int):
        return f"line-{camera_source + 1}"

    source_str = str(camera_source).strip()
    if source_str.isdigit():
        return f"line-{int(source_str) + 1}"

    m = re.search(r"/dev/video(\d+)$", source_str)
    if m:
        return f"line-{int(m.group(1)) + 1}"

    normalized = re.sub(r"[^a-zA-Z0-9]+", "-", source_str).strip("-").lower()
    if normalized:
        return f"line-{normalized}"

    digest = hashlib.sha1(source_str.encode("utf-8")).hexdigest()[:8]
    return f"line-{digest}"


