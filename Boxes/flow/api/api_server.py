@app.post("/api/reports/open", response_model=ReportResponse)
async def open_report(body: OpenReportRequest) -> ReportResponse:
    """Open a new headless detection report. report_id is generated by the frontend."""
    resolved_production_line_id = (
        body.production_line_id.strip()
        if body.production_line_id and body.production_line_id.strip()
        else _production_line_from_camera_source(body.camera_source)
    )

    logger.info("=" * 60)
    logger.info("OPEN SESSION START")
    logger.info("Request Context:")
    logger.info("  - report_id:          %s", body.report_id)
    logger.info("  - session_id:         %s", body.report_id)  # report_id acts as session_id
    logger.info("  - production_line_id: %s", resolved_production_line_id)
    logger.info("  - camera_source:      %s", body.camera_source)
    logger.info("  - target_speed:       %s", body.target_speed)
    logger.info("  - max_temp:           %s", body.max_temp)
    logger.info("  - max_amps:           %s", body.max_amps)
    logger.info("=" * 60)
    
    try:
        loop = asyncio.get_running_loop()

        worker = session_manager.create_session(
            report_id=body.report_id,
            camera_source=body.camera_source,
            production_line_id=resolved_production_line_id,
            target_speed=body.target_speed,
            max_temp=body.max_temp,
            max_amps=body.max_amps,
            box_cfg=configs["box"],
            defect_cfg=configs["defect"],
            stream_cfg=configs["stream"],
            app_cfg=configs["app"],
            loop=loop,
        )
        return ReportResponse(
            status="success",
            report_id=worker.report_id,
            message=f"Report started with camera {body.camera_source}",
        )
    except ValueError as e:
        logger.warning("[Error] open report failed: %s", e)
        raise HTTPException(status_code=400, detail=str(e))
    except Exception as e:
        logger.exception("[Error] open report")
        raise HTTPException(status_code=500, detail="Internal server error")


@app.post("/api/reports/close", response_model=ReportResponse)
async def close_report(body: CloseReportRequest) -> ReportResponse:
    """Close an active detection report by report_id."""
    try:
        closed, already_closed = session_manager.close_session(body.report_id)
        if already_closed:
            return ReportResponse(
                status="success",
                report_id=body.report_id,
                message="Report is already closed",
            )
        return ReportResponse(
            status="success",
            report_id=body.report_id,
            message="Report closed successfully",
        )
    except HTTPException:
        raise
    except Exception as e:
        logger.exception("[Error] close report")
        raise HTTPException(status_code=500, detail="Internal server error")


@app.get("/api/reports", response_model=List[ReportSummary])
async def list_reports() -> List[ReportSummary]:
    """List only currently active (open) reports. Format: report_id, viewers_count."""
    try:
        sessions = session_manager.list_active_sessions()
        return [ReportSummary(**s) for s in sessions]
    except Exception as e:
        logger.exception("[Error] list reports")
        raise HTTPException(status_code=500, detail="Internal server error")


@app.get("/api/health", response_model=HealthResponse)
async def health_check() -> HealthResponse:
    """Service health and active report count."""
    try:
        active = len(session_manager.list_active_sessions())
        return HealthResponse(status="healthy", active_reports=active)
    except Exception as e:
        logger.exception("[Error] health check")
        raise HTTPException(status_code=500, detail="Internal server error")


